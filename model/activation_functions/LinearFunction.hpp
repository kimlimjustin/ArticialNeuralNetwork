#ifndef LinearFunction_hpp
#define LinearFunction_hpp

#include "ActivationFunction.hpp"

using namespace std;

//-------------------------------------------------------------
//【类名】LinearFunction
//【功能】线性激活函数实现
//【说明】输出值等于输入值，即恒等函数
//【开发者及日期】林钲凯 2025-07-27
//【更改记录】
//-------------------------------------------------------------
class LinearFunction : public ActivationFunction {
public:
    //-------------------------------------------------------------
    //【函数名称】LinearFunction
    //【函数功能】默认构造函数
    //【参数】无
    //【返回值】无
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    LinearFunction() = default;
    
    //-------------------------------------------------------------
    //【函数名称】LinearFunction（拷贝构造）
    //【函数功能】拷贝构造函数
    //【参数】other：被拷贝的Linear函数
    //【返回值】无
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    LinearFunction(const LinearFunction& other) = default;
    
    //-------------------------------------------------------------
    //【函数名称】operator=
    //【函数功能】赋值运算符重载
    //【参数】other：赋值来源Linear函数
    //【返回值】LinearFunction&，自身引用
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    LinearFunction& operator=(const LinearFunction& other) = default;
    
    //-------------------------------------------------------------
    //【函数名称】~LinearFunction
    //【函数功能】析构函数
    //【参数】无
    //【返回值】无
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    ~LinearFunction() = default;
    
    //-------------------------------------------------------------
    //【函数名称】activate
    //【函数功能】线性激活变换（恒等函数）
    //【参数】x：输入值
    //【返回值】double，直接返回输入值
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    double activate(double x) const override;
    
    //-------------------------------------------------------------
    //【函数名称】getName
    //【函数功能】获取函数名称
    //【参数】无
    //【返回值】string，函数名"Linear"
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    string getName() const override;
    
    //-------------------------------------------------------------
    //【函数名称】clone
    //【函数功能】克隆函数对象
    //【参数】无
    //【返回值】unique_ptr<ActivationFunction>，克隆对象
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    unique_ptr<ActivationFunction> clone() const override;
};

#endif // LinearFunction_hpp
