#include "SigmoidFunction.hpp"
#include <cmath>

using namespace std;

//-------------------------------------------------------------
//【函数名称】activate
//【函数功能】Sigmoid 激活函数实现
//【参数】x：输入值
//【返回值】double，激活后输出
//【开发者及日期】林钲凯 2025-07-27
//【更改记录】
//-------------------------------------------------------------
double SigmoidFunction::activate(double x) const {
    return 1.0 / (1.0 + exp(-x));
}

//-------------------------------------------------------------
//【函数名称】getName
//【函数功能】获取函数名称
//【参数】无
//【返回值】string，函数名
//【开发者及日期】林钲凯 2025-07-27
//【更改记录】
//-------------------------------------------------------------
string SigmoidFunction::getName() const {
    return "Sigmoid";
}

//-------------------------------------------------------------
//【函数名称】clone
//【函数功能】克隆自身
//【参数】无
//【返回值】unique_ptr<ActivationFunction>
//【开发者及日期】林钲凯 2025-07-27
//【更改记录】
//-------------------------------------------------------------
unique_ptr<ActivationFunction> SigmoidFunction::clone() const {
    return unique_ptr<ActivationFunction>(new SigmoidFunction());
}
