#ifndef TanhFunction_hpp
#define TanhFunction_hpp

#include "ActivationFunction.hpp"

//-------------------------------------------------------------
//【类名】TanhFunction
//【功能】双曲正切激活函数实现
//【说明】输出值在 -1 到 1 之间
//【开发者及日期】林钲凯 2025-07-27
//【更改记录】
//-------------------------------------------------------------
class TanhFunction : public ActivationFunction {
public:
    //-------------------------------------------------------------
    //【函数名称】TanhFunction
    //【函数功能】默认构造函数
    //【参数】无
    //【返回值】无
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    TanhFunction() = default;
    
    //-------------------------------------------------------------
    //【函数名称】TanhFunction（拷贝构造）
    //【函数功能】拷贝构造函数
    //【参数】other：被拷贝的Tanh函数
    //【返回值】无
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    TanhFunction(const TanhFunction& other) = default;
    
    //-------------------------------------------------------------
    //【函数名称】operator=
    //【函数功能】赋值运算符重载
    //【参数】other：赋值来源Tanh函数
    //【返回值】TanhFunction&，自身引用
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    TanhFunction& operator=(const TanhFunction& other) = default;
    
    //-------------------------------------------------------------
    //【函数名称】~TanhFunction
    //【函数功能】析构函数
    //【参数】无
    //【返回值】无
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    ~TanhFunction() = default;
    
    //-------------------------------------------------------------
    //【函数名称】activate
    //【函数功能】双曲正切激活变换
    //【参数】x：输入值
    //【返回值】double，Tanh 输出（-1到1之间）
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    double activate(double x) const override;
    
    //-------------------------------------------------------------
    //【函数名称】getName
    //【函数功能】获取函数名称
    //【参数】无
    //【返回值】std::string，函数名"Tanh"
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    std::string getName() const override;
    
    //-------------------------------------------------------------
    //【函数名称】clone
    //【函数功能】克隆函数对象
    //【参数】无
    //【返回值】std::unique_ptr<ActivationFunction>，克隆对象
    //【开发者及日期】林钲凯 2025-07-27
    //【更改记录】
    //-------------------------------------------------------------
    std::unique_ptr<ActivationFunction> clone() const override;
};

#endif // TanhFunction_hpp
